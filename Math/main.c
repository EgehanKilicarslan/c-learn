#include <stdlib.h>
#include <stdio.h>
#include <math.h>

int main(){


    /* 
    ceil = round up
    floor = round down
    pow = power
    sqrt = square root
    abs = absolute value
    */ 

    //Examples
    printf("ceil: %f\n", ceil(36.356));
    printf("floor: %f\n", floor(36.356));
    printf("pow: %f\n", pow(2, 3));
    printf("sqrt: %f\n", sqrt(36));
    printf("abs: %d\n", abs(-36));
    
    system("pause");
    return 0;
};